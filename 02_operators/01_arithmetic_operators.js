/**
 * Used to perform mathematical calculations:
    +  : (Addition): Adds two numbers.
    -  : (Subtraction): Subtracts one number from another.
    *  : (Multiplication): Multiplies two numbers.
    /  : (Division): Divides one number by another.
    %  : (Remainder/Modulus): Returns the division remainder (e.g., 10 % 3 is 1).
    ** : (Exponentiation): Raises a base number to an exponent power (e.g., 2 ** 3 is 8).
    ++ : (Increment): Increases a number by one (can be prefix or postfix).
    -- : (Decrement): Decreases a number by one (can be prefix or postfix).
 */

console.log(5 + 2);     // 7
console.log(5 - 2);     // 3
console.log(5 * 2);     // 10
console.log(5 / 2);     // 2.5
console.log(5 % 2);     // 1
console.log(5 ** 2);    // 25

let x = 6;
console.log(x++);       // 6
console.log(x);         // 7

x = 6;
console.log(++x);       // 7

x = 6;
console.log(x--);       // 6
console.log(x);         // 5

x = 6;
console.log(--x);       // 5





